# Создайте словарь со списком вещей для похода в качестве ключа и их массой в качестве значения. 
# Определите какие вещи влезут в рюкзак передав его максимальную грузоподъёмность. Достаточно вернуть один допустимый вариант.
# *Верните все возможные варианты комплектации рюкзака.


from operator import itemgetter

my_diction = {'аптечка': 1, 'палатка': 8, 'спальник': 2, 'еда': 2, 'вода': 3, 'мангал': 5, 'дождевик': 4, 'зубная щетка': 10}
max_capacity_backpack = float(input("Введите максимальную грузоподъёмность рюкзака: "))
weight = 0
capacity_backpack = 0
print("Что взять в поход: ", my_diction)
print("Вещи которые влезут в рюкзак", max_capacity_backpack, "кг")
for things, value in dict(sorted(my_diction.items(), key=itemgetter(1))).items():
    weight += my_diction[things]

    if weight <= max_capacity_backpack:
        print(things, ' = ', value)
        capacity_backpack += my_diction[things]

print("Общий вес рюкзака c вещами составит: ", capacity_backpack)
