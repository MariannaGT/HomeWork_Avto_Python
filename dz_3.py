# В результирующем списке не должно быть дубликатов.

myList = [10, 5, 4, 7, 0, 9, 10, 2, 5, 5, 4, 6, 7, 8,1]
dubList = []
res = []
for item in myList:
    if myList.count(item) > 1:
        dubList.append(item)
    else:
        res.append(item)
print(f"Список повторяющихся элементов: \n{dubList}")
print(f"Результирующий список (дубликаты отсуствуют): \n{res}")


